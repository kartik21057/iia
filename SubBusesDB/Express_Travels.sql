drop database If exists Express_Travels;

create database Express_Travels;

use Express_Travels;

-- drop table bus_Stations
create table bus_stations(
Station_ID int Primary Key AUTO_INCREMENT,
Station_Name varchar(50),
City varchar(50),
Country varchar(50),
Area varchar(50)
);

-- drop table Bus_Passengers
create table Bus_Passengers(
Passenger_ID int primary key AUTO_INCREMENT,
F_Name varchar(50),
L_Name varchar(50),
Email varchar(50),    
Gender VARCHAR(10),
Phone_Number varchar(20)
);

-- drop table Driver
create table Driver(
Driver_ID int primary key AUTO_INCREMENT,
Driver_Name varchar(50),
Rating double,
Phone_Number varchar(20)
);

-- drop table Buses
create table Buses(
Bus_ID int primary key AUTO_INCREMENT,
Bus_Name varchar(80),
Bus_Number_Plate varchar(50),
Bus_Color varchar(20),
Bus_Brand varchar(30),
Class varchar(20),
Departure_Time datetime,
Arrival_Time datetime,
Duration INT,
Price double,
Origin_Station_ID int,
Destination_Station_ID int,
Driver_ID int,
Capacity int,
Foreign Key(Origin_Station_ID) references bus_stations(Station_ID),
foreign key(Destination_Station_ID) references bus_stations(Station_ID),
foreign key(Driver_ID) references Driver(Driver_ID)
);

-- drop table Bus_Bookings
create table Booking(
Booking_ID int Primary Key AUTO_INCREMENT,
Booking_Datetime datetime,
Total_amount double,
Passenger_ID int,
Bus_ID int,
Booked_By VARCHAR(10),
Foreign Key(Passenger_ID) references Bus_Passengers(Passenger_ID),
Foreign key(Bus_ID) references Buses(Bus_ID)
);

